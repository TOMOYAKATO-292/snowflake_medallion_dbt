-- =============================================================================
-- カスタムテスト: 未来日チェック (Chapter 5 で学ぶ)
-- =============================================================================
-- 【カスタムテスト（singular test）とは】
-- tests/ ディレクトリに置く SQL ファイル。
-- スキーマテスト（_staging_models.yml で定義するもの）では表現できない
-- 複雑な条件をテストできる。
--
-- 【このテストのルール】
-- dbt のテストは「違反行を返す SELECT」として書く。
-- - 0行返す → テスト PASS（違反なし）
-- - 1行以上返す → テスト FAIL（違反あり）
--
-- つまり「問題のあるデータを検索するクエリ」を書けばよい。
--
-- 【このテストの内容】
-- 購買トランザクション (stg_transactions) に未来の日付のレコードがないことを確認する。
-- 未来日のデータがあれば、データ品質の問題を示している。
--
-- 【実行方法】
-- dbt test --select assert_no_future_dates
-- =============================================================================

SELECT *
FROM {{ ref('stg_transactions') }}
WHERE transaction_date > CURRENT_TIMESTAMP()
